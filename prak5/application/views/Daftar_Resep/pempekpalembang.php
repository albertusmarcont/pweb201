			<h2>Pempek Palembang</h2>
			<p>
				Pempek atau empek-empek adalah makanan yang terbuat dari daging ikan yang digiling lembut yang dicampur tepung kanji atau tepung sagu,
			  serta komposisi beberapa bahan lain seperti telur, bawang putih yang dihaluskan, penyedap rasa, dan garam.
			  Pempek biasanya disajikan dengan kuah cuka yang memiliki rasa asam, manis, dan pedas.
			  Pempek sering disebut sebagai makanan khas Palembang, meskipun hampir semua daerah di Sumatera Selatan memroduksinya.
			</p>

			<br>
					<h3>Bahan-bahan:</h3>
					<ul>
						<li>500 gr ikan tenggiri giling</li>
					  <li>1 butir putih telur</li>
					  <li>250 ml air</li>
					  <li>1 sdm garam</li>
					  <li>1 sdt penyedap jamur</li>
					  <li>350 gr tepung sagu</li>
					  <li>Air secukupnya untuk merebus</li>
					  <li>Telur secukupnya (untuk isian)</li>
					</ul>

      <br>
			<h3>Bahan Kulit Pempek:</h3>
				<ul>
			  	<li>400 gram kulit ikan tenggiri</li>
			  	<li>1,5 sendok makan gula pasir</li>
			  	<li>1,5 sendok teh garam</li>
			  	<li>1/2 sendok teh merica bubuk</li>
			  	<li>5 siung bawang putih dihaluskan</li>
			  	<li>125-150 ml air dingin</li>
			  	<li>300 gram tepung sagu/tapioka</li>
				</ul>

      <br>
				<h3>Bahan cuko:</h3>
				<ul>
			  	<li>300 gram gula merah</li>
			  	<li>700 ml air</li>
			  	<li>1 sendok teh gula pasir</li>
			  	<li>8 bawang putih</li>
			  	<li>10 cabai rawit merah</li>
			  	<li>1 sendok makan asam jawa dilarutkan dengan sedikit air</li>
			  	<li>Sedikit garam</li>
			  	<li>Sedikit cuka putih</li>
				</ul>

				<br>
					<h3>Cara Membuat Pempek Kulit:</h3>
					<ol>
					  <li>Haluskan kulit ikan tenggiri yang masih setengah beku dengan food processor atau penggiling.</li>
					  <li>Campur dengan bahan lainnya kecuali tepung sagu. Aduk rata.</li>
					  <li>Campurkan tepung sagu sedikit demi sedikit.</li>
					  <li>Balurkan tepung sagu di tangan dan di talenan. Sendokkan adonan pempek, gulingkan di talenan lalu bentuk pipih. Lakukan sampai adonan habis.</li>
					  <li>Goreng pempek dengan api sedang dan sudah panas agar pempek garing dan tidak menyerap minyak terlalu banyak. Adonan yang belum digoreng bisa disimpan di chiller.</li>
					  <li>Lalu buat sambal cukonya. Haluskan bawang putih dan cabai rawit, campur dengan cuka dan garam. Diamkan hingga 30 menit.</li>
					  <li>Lalu masak dengan bahan lainnya agak lama hingga mendidih dan agak kental. Diamkan semalaman dan saring. Sajikan pempek kulit dengan sambal cuko.</li>
					</ol>

				<br>
					<h3>Cara Membuat:</h3>
					<ol>
						<li>Masukkan ikan ke dalam baskom, beri putih telur, air, garam dan penyedap jamur, aduk rata</li>
						<li>Masukkan tepung sagu, aduk rata kembali</li>
						<li>Ambil 150 gr adonan, beri isian 1 butir telur</li>
						<li>Masukkan adonan yang sudah dibentuk ke dalam air mendidih, masak hingga mengapung.</li>
						<li>Angkat dan tiriskan dengan cuko pempek.</li>
					</ol>

