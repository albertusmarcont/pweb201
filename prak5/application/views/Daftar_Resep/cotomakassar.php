			<h2>Coto Makassar</h2>
			<p>
				Makanan khas makassar yang kaya rempah ini benar-benar sangat populer di semua penjuru Nusantara bahkan juga Mancanegara.
			  Bagi banyak orang yang datang ke Makassar tentu pernah mencicipi coto makassar ini.
			</p>

			<br>
			<h3>Bahan-bahan:</h3>
			<ul>
				<li>Daging sapi - 500 gram</li>
			  <li>Kacang, sangrai lalu haluskan - 200 gram</li>
			  <li>Air cucian beras - 1 liter</li>
			  <li>Garam - 1 sdm</li>
			  <li>Kaldu bubuk - 1 sdt</li>
			  <li>Air, untuk merebus daging - secukupnya</li>
			  <li>Minyak, untuk menumis - secukupnya</li>
			  <li>Daun bawang - secukupnya</li>
			  <li>Daun seledri - secukupnya</li>
			  <li>Bawang goreng - secukupnya</li>
			  <li>Jeruk nipis - secukupnya</li>
			  <li>Sambal tauco - secukupnya</li>
			</ul>

			<br>
			<h3>Bumbu Halus:</h3>
			<ul>
				<li>Bawang putih - 10 siung</li>
			  <li>Bawang merah - 15 siung</li>
			  <li>Serai, ambil putihnya - 3 batang</li>
			  <li>Lengkuas - 1 cm</li>
			  <li>Kemiri - 3 butir</li>
			  <li>Ketumbar - 1/2 sdm</li>
			  <li>Merica - 1 sdt</li>
			  <li>Jintan - 1/2 sdt</li>
			</ul>

			<br>
			<h3>Cara Membuat:</h3>
			<ol>
			  <liDalam panci, rebus daging dengan air hingga empuk. Angkat daging, lalu potong jadi bentuk kotak/dadu, dan sisihkan. Ambil sebagian air sisa rebusan daging.</li>
			  <li>Dalam panci, didihkan air rebusan daging bersama air beras.</li>
			  <li>Sementara itu, panaskan minyak dan tumis bumbu halus hingga harum. Masukkan kacang tanah yang sudah dihaluskan, lalu aduk rata.</li>
			  <li>Angkat tumisan dan masukkan dalam rebusan air kuah. Aduk rata dan biarkan hingga mendidih.</li>
			  <li>Bumbui dengan garam dan penyedap rasa. Aduk rata lalu koreksi rasanya. Matikan api.</li>
			  <li>Penyajian: Dalam mangkok, tata potongan daging. Beri daun bawang, daun seledri, dan bawang goreng. Siram dengan kuah coto.</li>
			  <li>Siap disajikan dengan buras, jeruk nipis, dan sambal tauco.</li>
			</ol>

