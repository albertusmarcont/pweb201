			<h2>Konro</h2>
			<p>
				Konro yaitu masakan tradisional khas Makassar.
			  Memiliki bahan dasar iga sapi atau daging sapi.
			  Daging sapi direbus bersama dengan bahan lain seperti kayu manis, air asam jawa serta beragam bahan yang lain.
			  Lalu tumisan campuran beberapa bumbu masak seperti merica, pala, kacang merah serta bahan yang lain,
			  dituangkan dalam rebusan iga sapi. Warna gelap sop konro sendiri berasal dari buah kluwak yang memang berwarna hitam.
			  Konro aslinya dimasak berkuah berbentuk sup yang kaya rempah-rempah.
			</p>

			<br>
			<h3>Bahan-bahan:</h3>
			<ul>
				<li>Iga Sapi - 1 kg</li>
			  <li>Air - 1,5 liter</li>
			  <li>Bawang merah, iris - 2 siung</li>
			  <li>Serai, memarkan - 1 batang</li>
			  <li>Jahe, memarkan - 2 cm</li>
			  <li>Daun salam - 3 lembar</li>
			  <li>Daun jeruk - 3 lembar</li>
			  <li>Kayu manis - 1/2 sdt</li>
			  <li>Kapulaga - 1 buah</li>
			  <li>Cengkeh - 4 buah</li>
			  <li>Gula merah - 1 sdm</li>
			  <li>Kaldu bubuk sapi - 1 sdt</li>
			  <li>Garam - secukupnya</li>
			</ul>



        <br>
        <h3>Bumbu Halus:</h3>
        <ul>
					<li>Bawang merah - 6 butir</li>
				  <li>Bawang putih - 3 siung</li>
				  <li>Kluwak, rendam air panas - 3 buah</li>
				  <li>Kemiri - 3 butir</li>
				  <li>Ketumbar - 1 sdt</li>
				  <li>Jintan - 1/2 sdt</li>
				  <li>Pala bubuk - 1/2 sdt</li>
				  <li>Kunyit - 2 cm</li>
				  <li>Jahe - 3 cm</li>
				  <li>Biji lada - 1 sdt</li>
        </ul>

        <br>
				<h3>Cara Membuat:</h3>
				<ol>
				  <li>Dalam panci, rebus iga sapi bersama air. Buang kotoran dan minyak yang mengapung.</li>
				  <li>Matikan api setelah daging empuk. Pisahkan iga sapi dengan air rebusannya (kaldu) lalu sisihkan.</li>
				  <li>Tumis bumbu halus bersama bahan-bahan lainnya hingga wangi dan tidak langu.</li>
				  <li>Tuang air rebusan (kaldu) iga dalam tumisan bumbu. Aduk rata.</li>
				  <li>Masukkan iga sapi, gula merah, kaldu bubuk, dan garam. Masak sampai bumbu meresap dan kuah agak mengental. Koreksi rasanya lalu angkat.</li>
				  <li>Sajikan dengan taburan bawang goreng dan irisan daun bawang.</li>
				</ol>

