		<h2>Ayam Bumbu Bali</h2>
			<p>
				Ayam Bumbu Bali merupakan salah satu kuliner khas yang dapat kita temukan di Bali dan memiliki cita rasa luar biasa sedap.
			</p>

			<br>
			<h3>Bahan-bahan:</h3>
			<ul>
				<li>1 ekor ayam, potong-potong</li>
			  <li>1 sendok teh gula pasir</li>
			  <li>1/4 sendok teh merica bubuk</li>
			  <li>2 sendok teh garam</li>
			  <li>3 lembar daun salam</li>
			  <li>3 sendok makan minyak untuk menumis</li>
			  <li>700 ml air</li>
			</ul>

			<br>
			<h3>Bumbu halus:</h3>
			<ul>
			  <li>1 buah tomat</li>
			  <li>1 sendok teh terasi, dibakar</li>
			  <li>10 butir bawang merah</li>
			  <li>4 siung bawang putih</li>
			  <li>6 buah cabai merah besar</li>
			  <li>8 buah cabai merah keriting</li>
			</ul>

			<br>
			<h3>Cara Membuat:</h3>
			<ol>
			  <li>Panaskan minyak.</li>
			  <li>Masukkan bumbu halus dan daun salam, tumis hingga harum.</li>
			  <li>Masukkan ayam lalu aduk hingga berubah warna.</li>
			  <li>Tambahkan garam, gula, dan merica. Aduk rata.</li>
			  <li>Tambahkan air.</li>
			  <li>Masak hingga matang dan bumbunya meresap.</li>
			  <li>Ayam bumbu Bali siap disajikan.</li>
			</ol>

