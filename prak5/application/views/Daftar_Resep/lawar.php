					<h2>Lawar</h2>
					<p>
						Lawar adalah masakan berupa campuran sayur-sayuran dan daging cincang yang dibumbui secara merata yang berasal dari Bali.
				    Makanan ini lazim disajikan dalam rumah tangga di Bali atau dijual secara luas di rumah-rumah makan dengan sebutan lawar Bali.
				    Lawar dibuat dari daging yang dicincang, sayuran, sejumlah bumbu-bumbu dan kelapa.
				    Kadang-kadang di beberapa jenis lawar diberikan unsur yang dapat menambah rasa dari lawar itu yaitu darah dari daging itu sendiri.
          </p>

					<br>
					<h3>Bahan-bahan:</h3>
					<ul>
						<li>100 gram kelapa parut (setengah tua)</li>
				    <li>150 gram daging ayam rebus, disuwir</li>
				    <li>2 sendok makan gula merah, disisir</li>
				    <li>2 sendok makan minyak untuk menumis</li>
				    <li>3 sendok makan bawang goreng</li>
				    <li>1 buah jeruk nipis</li>
				    <li>1 buah mentimun, potong dadu kecil</li>
				    <li>1 sendok teh garam</li>
				    <li>100 garam kacang panjang, potong 2 cm, lalu rebus setengah matang</li>
				    <li>100 gram kecambah, rebus</li>
					</ul>

                    <br>
										<h3>Bumbu halus:</h3>
									  <ul>
									    <li>1 cm jahe</li>
									    <li>1 cm lengkuas</li>
									    <li>1/2 blok terasi</li>
									    <li>1/2 sendok teh ketumbar</li>
									    <li>1/2 sendok teh lada</li>
									    <li>10 buah cabai rawit (atau sesuai selera)</li>
									    <li>2 butir kemiri</li>
									    <li>2 cm kencur</li>
									    <li>2 siung bawang putih</li>
									    <li>3 buah cabai merah, buang bijinya</li>
									    <li>6 butir bawang merah</li>
									  </ul>

                    <br>
										<h3>Cara Membuat:</h3>
									  <ol>
									    <li>Tumis bumbu halus sampai harum.</li>
									    <li>Masukkan garam, gula merah, dan tiga sendok makan air.</li>
									    <li>Masukkan daging ayam, kelapa parut, kacang panjang, dan kecambah.</li>
									    <li>Matikan kompor, masukkan irisan mentimun.</li>
									    <li>Sebelum dihidangkan, tambahkan air perasan jeruk nipis dan taburan bawang goreng.</li>
									  </ol>

