			<h2>Songkolo</h2>
				<p>
					Songkolo Bagadang merupakan makanan yang terbuat dari beras ketan putih yang dikukus hingga matang,
				  kadang juga menggunakan beras ketan hitam.
				  Songkolo atau orang Bugis menyebutnya "sokko" adalah makanan khas Makassar yang terbuat dari beras
        </p>

					<br>
					<h3>Bahan-bahan:</h3>
					<ul>
						<li>500 gr beras ketan.</li>
					  <li>200 gr santan kental.</li>
					  <li>1 bh Kelapa parut jgn terlalu tua ya buat serundeng.<li>
					  <li>5 siung bawang putih.</li>
					  <li>5 siung bawang merah.</li>
					  <li>1 sm ketumbar halus.</li>
					  <li>1 sm jinten halus.</li>
					  <li>3 bj kemiri.</li>
					  <li>secukupnya Air asam.</li>
					  <li>Gula merah.</li>
					  <li>secukupnya Garam.</li>
					</ul>

                    <br>
										<h3>Cara Membuat:</h3>
										<ol>
										  <li>Rendam beras ketan 1jam sesdh 1jam di kukus sampai stengah matang sesdh itu pindahkan beras ke wadah beri santan garam gula aduk rata kukus lagi sampai matang.</li>
										  <li>Campur kelapa parut bawang putih bawang merah kemiri halus ketumbar jintan halus sesdh tercampur rata aduk diatas kompor dgn api kecil sampai kelapa agak kering.</li>
										</ol>

