			<h2>Brenebon</h2>
			<p>
				Brenebon adalah masakan sup kacang merah khas Indonesia Timur,
				khususnya ditemukan pada khazanah masakan Minahasa.
				Hidangan sup ini dibuat dari kacang merah dan sayuran yang disajikan dalam kuah kaldu daging,
				dengan campuran bumbu sup pada umumnya seperti bawang putih, merica dan bumbu-bumbu lain.
      </p>

			<br>
			<h3>Bahan-bahan:</h3>
			<ul>
				<li>200 gram kacang merah</li>
			  <li>200 gram daging sapi</li>
			  <li>150 gram sosis sapi</li>
			  <li>3 butir cengkih</li>
			  <li>1 biji pala</li>
			  <li>1 batang seledri</li>
			  <li>1 batang daun bawang</li>
			  <li>air secukupnya</li>
			  <li>garam secukupnya</li>
			  <li>lada bubuk secukupnya</li>
			  <li>bawang goreng secukupnya</li>
			</ul>

			<h3>Cara Membuat:</h3>
			<ol>
			  <li>Rebus kacang merah hingga empuk.</li>
			  <li>Potong daging yang sudah direbus</li>
			  <li>Masukkan kacang merah ke dalam rebusan</li>
			  <li>Selanjutnya, biarkan sup termasak hingga kuahnya sedikit mengental.
			    Jika sudah, tambahkan potongan sosis, daun bawang, dan seledri ke dalamnya.</li>
			  <li>Aduk selama beberapa menit. Jika sudah matang, sajikan sup brenebon dengan taburan bawang goreng biar makin nikmat.</li>
			</ol>

