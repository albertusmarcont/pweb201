				<h2>Kapurung</h2>
				<p>
					Kapurung merupakan salah satu makanan khas tradisional di Sulawesi Selatan,
				  terutama masyarakat daerah Luwu (Kota Palopo, Kabupaten Luwu, Luwu Utara, Luwu Timur).
				</p>

        <br>
				<h3>Bahan-bahan:</h3>
				<ul>
					<li>Tepung sagu - 150 gram</li>
				  <li>Air - 450 ml</li>
				  <li>Jagung, pipil bijinya - 3 buah</li>
				  <li>Kacang panjang, potong-potong - 5 batang</li>
				  <li>Bayam, petik daunnya - 2 ikat</li>
				  <li>Air - 1 liter</li>
				  <li>Udang, rebus hingga matang - 10 ekor</li>
				  <li>Jeruk nipis, ambil air perasannya - 3 buah</li>
				  <li>Garam - 1 sdt</li>
				  <li>Penyedap rasa, opsional - 1/2 sdt</li>
				</ul>

				<br>
				<h3>Bumbu Halus:</h3>
				<ul>
					<li>Cabai rawit - 10 buah</li>
				  <li>Terasi udang, bakar - 1 bungkus</li>
				  <li>Kacang tanah, sangrai - 1 genggam</li>
				  <li>Fillet ikan cakalang - secukupnya</li>
				</ul>

				<br>
				<h3>CARA MEMBUAT ADONAN SAGU (KAPURUNG)</h3>
				<ol>
				  <li>Dalam wadah, campur tepung sagu dengan 300 ml air. Aduk rata lalu sisihkan.</li>
				  <li>Panaskan 150 ml sisa air hingga mendidih.</li>
				  <li>Masukkan larutan sagu ke dalam air mendidih, lalu aduk terus hingga mengental dan adonan menyerupai lem. Matikan api.</li>
				  <li>Siapkan 500 ml air matang dalam wadah.</li>
				  <li>Bulat-bulatkan adonan sagu dengan menggunakan 2 buah sumpit atau tusuk sate, lalu masukkan dalam wadah air.</li>
				</ol>

				<br>
				<h3>CARA MEMBUAT IKAN MASAK KUNING</h3>
				<ol>
				  <li>Dalam wadah, lumuri ikan dengan air asam, garam, dan kunyit hingga merata.</li>
				  <li>Tambahkan air, lalu masak ikan hingga matang. Angkat.</li>
				  <li>Ambil ikan lalu fillet dagingnya. Sisihkan dan satukan bersama bumbu halus lainnya.</li>
				  <li>Sisihkan kuahnya.</li>
				</ol>

				<br>
				<h3>CARA MEMBUAT SAYUR</h3>
				<ol>
				  <li>Dalam panci, didihkan air.</li>
				  <li>Masukkan jagung, lalu biarkan masak hingga setengah matang.</li>
				  <li>Masukkan kacang panjang. Masak hingga empuk.</li>
				  <li>Masukkan bayam. Masak hingga matang. Matikan api.</li>
				</ol>

