					<h2>Sate Lilit</h2>
					<p>
						Sate lilit merupakan salah satu kuliner yang dapat kita temukan di Bali dan memiliki cita rasa luar biasa sedap.
					  Seperti namanya, sate lilit adalah sate yang dibuat dengan cara melilitkan bahan baku pada tusuk sate.
					  Bahan baku dalam pembuatan sate lilit sendiri biasanya berupa ikan, daging babi, sapi, atau ayam.
          </p>

				<br>
					<h3>Bahan-bahan:</h3>
					<ul>
						<li>500 gram daging ayam giling</li>
				    <li>8 sdm kelapa parut</li>
				    <li>Batang serai sebagai tangkai sate secukupnya</li>
				    <li>Kaldu ayam bubuk sesuai selera</li>
				    <li>Garam secukupnya</li>
				    <li>Minyak untuk menggoreng</li>
					</ul>

        <br>
          <h3>Bumbu Halus:</h3>
          <ul>
						<li>5 buah cabai merah besar yang sudah dibuang isinya</li>
						<li>9 siung bawang merah</li>
						<li>5 siung bawang putih</li>
						<li>5 butir kemiri yang telah sangrai terlebih dahulu5 butir kemiri yang telah sangrai terlebih dahulu</li>
						<li>1 ruas kencur</li>
						<li>5 cm jahe</li>
					 	<li>5 cm kunyit</li>
						<li>5 cm lengkuas</li>
						<li>Merica sesuai selera</li>
						<li>1/2 sdt bubuk ketumbar</li>
          </ul>

        <br>
					<h3>Cara Membuat:</h3>
					<ol>
						<li>Campurkan semua bahan dalam wadah</li>
						<li>Lilitkan adonan pada batang serai</li>
						<li>Siapkan teflon, kemudian tuangkan sedikit minyak goreng ke atas teflon. Nyalakan apinya dengan panas sedang. Setelah minyak cukup panas, segera masukkan lilitan sate, bolak-balik setiap sisi sate dan masak hingga matang sempurna.</li>
						<li>Setelah matang, segera angkat sate lilit dan sajikan hangat-hangat. Atau, jika kamu mau, kamu bisa memanggang kembali sate lilit yang telah dimasak menggunakan teflon.</li>
						<li>Setelah dipanggang, sate lilit siap dinikmati!</li>
					</ol>

